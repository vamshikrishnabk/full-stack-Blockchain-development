// for celsius to fahrenheit
let celsius = 60
let fahrenheit = (celsius * 9)/5  + 32

// for fahrenheit to celsius
let fahrenheit1 = 45
let celsius1 = ((fahrenheit1 - 32 )/9 ) * 5

console.log(`${celsius} degC is ${fahrenheit} degF`)
console.log(`${fahrenheit1} degF is ${celsius1} degC`)


